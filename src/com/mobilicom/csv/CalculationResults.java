package com.mobilicom.csv;

/**
 *
 * @author dhiraj
 */
public class CalculationResults {
    String correct;
    String anteena;
    String switching;
    String gain;
    String delta;
    String elevationDelta;
    String bearinDelta;

    public String getCorrect() {
        return correct;
    }

    public void setCorrect(String correct) {
        this.correct = correct;
    }

    public String getAnteena() {
        return anteena;
    }

    public void setAnteena(String anteena) {
        this.anteena = anteena;
    }

    public String getSwitching() {
        return switching;
    }

    public void setSwitching(String switching) {
        this.switching = switching;
    }

    public String getGain() {
        return gain;
    }

    public void setGain(String gain) {
        this.gain = gain;
    }

    public String getDelta() {
        return delta;
    }

    public void setDelta(String delta) {
        this.delta = delta;
    }

    public String getElevationDelta() {
        return elevationDelta;
    }

    public void setElevationDelta(String elevationDelta) {
        this.elevationDelta = elevationDelta;
    }

    public String getBearinDelta() {
        return bearinDelta;
    }

    public void setBearinDelta(String bearinDelta) {
        this.bearinDelta = bearinDelta;
    }
    
}
